class Solution(object):
    def isAnagram(self, s, t):
        """
        :type s: str
        :type t: str
        :rtype: bool
        """

        if len(s) != len(t):
            return False
        
        for i in range(len(s)):
            if s.count(s[i]) != t.count(s[i]):
                return False
        return True

